public class Salon {
    private String numeSalon;
    private int capacitateSalon;

    public Salon(String numeSalon, int capacitateSalon) {
        this.numeSalon = numeSalon;
        this.capacitateSalon = capacitateSalon;
    }

    public String getNumeSalon() {
        return numeSalon;
    }

    public void setNumeSalon(String numeSalon) {
        this.numeSalon = numeSalon;
    }

    public int getCapacitateSalon() {
        return capacitateSalon;
    }

    public void setCapacitateSalon(int capacitateSalon) {
        this.capacitateSalon = capacitateSalon;
    }
}
