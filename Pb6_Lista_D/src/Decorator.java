public abstract class Decorator extends ModalitatePlata{

    protected ModalitatePlata modalitatePlata;

    public Decorator(ModalitatePlata modalitatePlata) {
        this.modalitatePlata = modalitatePlata;
    }

    @Override
    public void adaugaModalitatePlata() {
        super.adaugaModalitatePlata();
    }
}
